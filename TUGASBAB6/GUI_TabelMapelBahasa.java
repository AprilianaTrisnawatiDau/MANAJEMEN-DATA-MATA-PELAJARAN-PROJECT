    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
      try {
        String jam = txtJam.getText();
        String kelas = txtKelas.getText();
        String materi = txtMateri.getText();
        String tingkat = (String) cmb_tingkat.getSelectedItem();
        String bahasa = "";
        if (Inggris.isSelected()) bahasa += "Inggris";
        if (Jepang.isSelected()) bahasa += "Jepang";

        MataPelajaranBahasa mp = new MataPelajaranBahasa();
        mp.setDataJam(jam);
        mp.setDataKelas(kelas);
        mp.setDataTingkat(tingkat);
        mp.setDataMateri(materi);
        mp.setBahasa(bahasa);

        listData.add(mp);

        DefaultTableModel model = (DefaultTableModel) tabel_data.getModel();
        model.addRow(new Object[]{
            mp.cetakJam(),
            mp.cetakKelas(),
            mp.cetakBahasa(),
            mp.cetakTingkat(),
            mp.cetakMateri()
        });

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
        batal();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

