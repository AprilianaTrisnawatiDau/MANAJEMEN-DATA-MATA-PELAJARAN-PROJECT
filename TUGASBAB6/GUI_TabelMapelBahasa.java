    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
    int selectedRow = tabel_data.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diubah.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            // Ambil data dari input form
            String jam = txtJam.getText();
            String kelas = txtKelas.getText();
            String materi = txtMateri.getText();
            String tingkat = (String) cmb_tingkat.getSelectedItem();
            String bahasa = "";
            if (Inggris.isSelected()) bahasa += "Inggris ";
            if (Jepang.isSelected()) bahasa += "Jepang";

            // Ambil objek MataPelajaranBahasa yang dipilih
            MataPelajaranBahasa mp = listData.get(selectedRow);

            // Update data objek mp
            mp.setDataJam(jam);
            mp.setDataKelas(kelas);
            mp.setDataMateri(materi);
            mp.setDataTingkat(tingkat);
            mp.setBahasa(bahasa);

            // Update baris di tabel dengan data yang baru
            DefaultTableModel model = (DefaultTableModel) tabel_data.getModel();
            model.setValueAt(mp.cetakJam(), selectedRow, 0);
            model.setValueAt(mp.cetakKelas(), selectedRow, 1);
            model.setValueAt(mp.cetakBahasa(), selectedRow, 2);
            model.setValueAt(mp.cetakTingkat(), selectedRow, 3);
            model.setValueAt(mp.cetakMateri(), selectedRow, 4);
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            batal(); // Bersihkan form input
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

