    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
     String kodeCari = txtCari.getText().trim();

    // Periksa apakah input pencarian kosong
    if (kodeCari.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan Kelas Bahasa yang ingin dicari.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        System.out.println("Pesan peringatan input kosong ditampilkan."); // Tambahkan untuk debugging
        return;
    }

    // Dapatkan model tabel dan bersihkan tabel sebelum menampilkan hasil pencarian
    DefaultTableModel model = (DefaultTableModel) tabel_data.getModel();
    model.setRowCount(0); // Kosongkan tabel satu kali sebelum loop

    // Flag untuk menandakan apakah data ditemukan
    boolean ditemukan = false;

    // Iterasi seluruh data di listData
    for (int i = 0; i < listData.size(); i++) {
        MataPelajaranBahasa mp = listData.get(i);

        // Cek apakah nilai bahasa (dari method cetakbahasa()) sama dengan yang dicari (case-insensitive)
        if (mp.cetakBahasa().equalsIgnoreCase(kodeCari)) {
            // Buat baris data berdasarkan atribut dari objek
            Object[] row = new Object[]{
                    mp.cetakJam(),
                    mp.cetakKelas(),
                    mp.cetakBahasa(),
                    mp.cetakTingkat(),
                    mp.cetakMateri()
            };
            // Tambahkan baris ke model tabel
            model.addRow(row);
            ditemukan = true;
        }
    }

    // Jika tidak ditemukan satupun data yang cocok, tampilkan pesan
    if (!ditemukan) {
        JOptionPane.showMessageDialog(this, "Data dengan kode tersebut tidak ditemukan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnCariActionPerformed

