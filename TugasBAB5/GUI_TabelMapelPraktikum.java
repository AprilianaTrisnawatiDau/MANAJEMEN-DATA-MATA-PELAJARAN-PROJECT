    private void tampilkanData() {
        DefaultTableModel model = (DefaultTableModel) tabel_data.getModel();
        model.setRowCount(0);
        for (MataPelajaranPraktikum mp : listData) {
            Object[] rowData = {
                mp.cetakKelasAsal(),
                mp.cetakDurasi(),
                mp.cetakJumlahAslab(),
                mp.cetakKelas(),
                mp.cetakLaporan() + " | " +
                mp.cetakAlat() + " | " +
                mp.cetakModul() + " | ",
                mp.cetakStatus()
            };
            model.addRow(rowData);
        }
    }

