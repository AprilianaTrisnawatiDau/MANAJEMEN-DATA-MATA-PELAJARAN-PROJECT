    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
         int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menyimpan data ini?", "Konfirmasi Simpan", JOptionPane.YES_NO_OPTION);
         if (confirm == JOptionPane.YES_OPTION) {
            String kelasAsal = txtKelasAsal.getText();
            String durasi = txtDurasi.getText();
            String jumlahAslab = txtJumlahAslab.getText();
            String kelas = (String) cmb_kelas.getSelectedItem();
            boolean laporanChecked = chkLaporan.isSelected();
            boolean alatChecked = chkAlat.isSelected();
            boolean modulChecked = chkModul.isSelected();
            String statusPraktikum;
            String laporan = laporanChecked ? "‚úì" : "‚úó";
            String alat = alatChecked ? "‚úì" : "‚úó";
            String modul = modulChecked ? "‚úì" : "‚úó";

       

         if (!laporanChecked && !alatChecked && !modulChecked) {
        statusPraktikum = "DILARANG";
        } else if (laporanChecked && modulChecked && !alatChecked) {
         statusPraktikum = "DILARANG";
        } else if ((laporanChecked ^ alatChecked ^ modulChecked) && 
         !(laporanChecked && alatChecked) && 
         !(laporanChecked && modulChecked) && 
         !(alatChecked && modulChecked)) {
         statusPraktikum = "DILARANG";
        } else {
         statusPraktikum = "DIIZINKAN";
        }


            MataPelajaranPraktikum mp = new MataPelajaranPraktikum();
            mp.setKelasAsal(kelasAsal);
            mp.setDurasi(durasi);
            mp.setJumlahAslab(jumlahAslab);
            mp.setKelas(kelas);
            mp.setLaporan(laporan);
            mp.setAlat(alat);
            mp.setModul(modul);
            mp.dataStatus = statusPraktikum; // Set status langsung di sini

            // Cek apakah data dengan kelas asal yang sama sudah ada
            int indexToUpdate = -1;
            for (int i = 0; i < listData.size(); i++) {
                if (listData.get(i).cetakKelasAsal().equals(kelasAsal)) {
                    indexToUpdate = i;
                    break;
                }
            }

            if (indexToUpdate != -1) {
                listData.set(indexToUpdate, mp); // Update data yang sudah ada
            } else {
                listData.add(mp); // Tambahkan data baru
            }

            tampilkanData();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            batal(); // Optional: bersihkan form setelah menyimpan
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

