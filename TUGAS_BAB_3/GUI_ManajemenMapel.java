String kategoriComboBox = (String) ComboKategori.getSelectedItem();
String jenisComboBox = (String) ComboKategori.getSelectedItem();
int semester = 0; // Initialize semester to 0

try {
    semester = Integer.parseInt(semesterStr); // Attempt to parse the String to an integer
} catch (NumberFormatException e) {
    memoCetak.setText("Error: Semester must be a valid integer.");
    return;
}

