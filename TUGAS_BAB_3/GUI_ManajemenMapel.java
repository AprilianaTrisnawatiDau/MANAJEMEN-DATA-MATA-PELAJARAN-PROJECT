                "Nama Mapel : " + nama + "\n" + 
                "Guru : " + guru  + "\n" +
                "Kategori: " + kategori + "\n" +
                "Semester: " + semester + "\n" +
                "Kategori : " + kategoriComboBox + "\n" ;

memoCetak.setText(output);
